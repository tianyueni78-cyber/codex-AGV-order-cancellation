clear
clc

testDir = fileparts(mfilename('fullpath'));
projectRoot = fileparts(testDir);

addpath(fullfile(projectRoot, 'configs'));

beforeFiles = dir(projectRoot);
beforeNames = sort({beforeFiles.name});

smallConfig = small_nsga2_config(projectRoot);
mediumConfig = medium_nsga2_config(projectRoot);
formalConfig = formal_nsga2_config(projectRoot);

assert_entry_config(smallConfig, projectRoot, 'small');
assert_entry_config(mediumConfig, projectRoot, 'medium');
assert_entry_config(formalConfig, projectRoot, 'formal');

assert(smallConfig.algorithm.pop <= 10, ...
    'small config pop should be <= 10.');
assert(smallConfig.algorithm.max_gen <= 2, ...
    'small config max_gen should be <= 2.');

assert(mediumConfig.algorithm.pop > smallConfig.algorithm.pop, ...
    'medium config should use a larger pop than small config.');
assert(mediumConfig.algorithm.max_gen > smallConfig.algorithm.max_gen, ...
    'medium config should use a larger max_gen than small config.');

assert(isfield(formalConfig.random, 'seedList'), ...
    'formal config should include random.seedList.');
assert(isfield(formalConfig.random, 'currentSeed'), ...
    'formal config should include random.currentSeed.');
assert(formalConfig.random.currentSeed == formalConfig.random.seed, ...
    'formal config currentSeed should be traceable to random.seed.');
assert(isfield(formalConfig, 'output'), 'formal config should include output.');
assert(formalConfig.output.saveSummary, ...
    'formal config should save summary by default.');
assert(formalConfig.output.saveMat, ...
    'formal config should save mat result by default.');
assert(formalConfig.output.saveRunInfo, ...
    'formal config should save run_info by default.');

afterFiles = dir(projectRoot);
afterNames = sort({afterFiles.name});
assert(isequal(beforeNames, afterNames), ...
    'test_experiment_entry_configs created or removed files in the project root.');

fprintf(['test_experiment_entry_configs passed: ', ...
    'small(pop=%d,max_gen=%d), medium(pop=%d,max_gen=%d), ', ...
    'formal(pop=%d,max_gen=%d,currentSeed=%g)\n'], ...
    smallConfig.algorithm.pop, smallConfig.algorithm.max_gen, ...
    mediumConfig.algorithm.pop, mediumConfig.algorithm.max_gen, ...
    formalConfig.algorithm.pop, formalConfig.algorithm.max_gen, ...
    formalConfig.random.currentSeed);

function assert_entry_config(config, projectRoot, label)
assert(isstruct(config), [label, ' config should be a struct.']);
assert(isfield(config, 'paths'), [label, ' config missing paths.']);
assert(isfield(config, 'random'), [label, ' config missing random.']);
assert(isfield(config, 'algorithm'), [label, ' config missing algorithm.']);
assert(isfield(config, 'energy'), [label, ' config missing energy.']);

requiredPathFields = {'fjsp', 'machineExcel', 'agvExcel', ...
    'algorithmDir', 'outputBaseDir'};
for i = 1:numel(requiredPathFields)
    fieldName = requiredPathFields{i};
    assert(isfield(config.paths, fieldName), ...
        [label, ' config paths missing field: ', fieldName]);
    assert(ischar(config.paths.(fieldName)) || ...
        isstring(config.paths.(fieldName)), ...
        [label, ' config path should be text: ', fieldName]);
    assert(~isempty(config.paths.(fieldName)), ...
        [label, ' config path is empty: ', fieldName]);
end

assert(isfile(config.paths.fjsp), ...
    [label, ' configured .fjs file does not exist.']);
assert(isfile(config.paths.machineExcel), ...
    [label, ' configured machine Excel file does not exist.']);
assert(isfile(config.paths.agvExcel), ...
    [label, ' configured AGV Excel file does not exist.']);
assert(isfolder(config.paths.algorithmDir), ...
    [label, ' configured algorithm directory does not exist.']);

outputsRoot = fullfile(projectRoot, 'outputs');
assert(startsWith(char(config.paths.outputBaseDir), char(outputsRoot)), ...
    [label, ' outputBaseDir should be under outputs/.']);

requiredAlgorithmFields = {'pop', 'max_gen', 'p_cross', 'p_mutation'};
for i = 1:numel(requiredAlgorithmFields)
    fieldName = requiredAlgorithmFields{i};
    assert(isfield(config.algorithm, fieldName), ...
        [label, ' config algorithm missing field: ', fieldName]);
    assert(isnumeric(config.algorithm.(fieldName)) && ...
        isscalar(config.algorithm.(fieldName)) && ...
        isfinite(config.algorithm.(fieldName)), ...
        [label, ' algorithm field should be finite: ', fieldName]);
end

assert(config.algorithm.pop > 0 && ...
    config.algorithm.pop == floor(config.algorithm.pop), ...
    [label, ' algorithm pop should be a positive integer.']);
assert(config.algorithm.max_gen > 0 && ...
    config.algorithm.max_gen == floor(config.algorithm.max_gen), ...
    [label, ' algorithm max_gen should be a positive integer.']);
assert(config.algorithm.p_cross >= 0 && config.algorithm.p_cross <= 1, ...
    [label, ' p_cross should be between 0 and 1.']);
assert(config.algorithm.p_mutation >= 0 && config.algorithm.p_mutation <= 1, ...
    [label, ' p_mutation should be between 0 and 1.']);

assert(isfield(config.random, 'seed'), ...
    [label, ' config random missing seed.']);
assert(isnumeric(config.random.seed) && isscalar(config.random.seed) && ...
    isfinite(config.random.seed), ...
    [label, ' random seed should be finite.']);

assert(isfield(config.energy, 'AGVEG_MAX'), ...
    [label, ' config energy missing AGVEG_MAX.']);
assert(isfield(config.energy, 'eChargeSpeed'), ...
    [label, ' config energy missing eChargeSpeed.']);
assert(config.energy.AGVEG_MAX > 0, ...
    [label, ' AGVEG_MAX should be positive.']);
assert(config.energy.eChargeSpeed > 0, ...
    [label, ' eChargeSpeed should be positive.']);
end
