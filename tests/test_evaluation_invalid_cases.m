    'evaluate_chromosome:MissingField');
expect_error(@() evaluate_chromosome(validChrom, ...
    rmfield(problem, 'machineNum'), machineData, agvData, config), ...
    'evaluate_chromosome:MissingField');
expect_error(@() evaluate_chromosome(validChrom, ...
