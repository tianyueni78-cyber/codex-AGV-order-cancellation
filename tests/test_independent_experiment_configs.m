clear
clc

testDir = fileparts(mfilename('fullpath'));
projectRoot = fileparts(testDir);

addpath(fullfile(projectRoot, 'configs'));

beforeFiles = dir(projectRoot);
beforeNames = sort({beforeFiles.name});

smallConfig = independent_small_config(projectRoot);
mediumConfig = independent_medium_config(projectRoot);
formalConfig = independent_formal_config(projectRoot);

assert_independent_config(smallConfig, projectRoot, ...
    'independent_small', 'independent_small_nsga2');
assert_independent_config(mediumConfig, projectRoot, ...
    'independent_medium', 'independent_medium_nsga2');
assert_independent_config(formalConfig, projectRoot, ...
    'independent_formal', 'independent_formal_nsga2');

assert(smallConfig.algorithm.pop <= 10, ...
    'independent small pop should be <= 10.');
assert(smallConfig.algorithm.max_gen <= 2, ...
    'independent small max_gen should be <= 2.');
assert(mediumConfig.algorithm.pop > smallConfig.algorithm.pop, ...
    'independent medium pop should be larger than small.');
assert(mediumConfig.algorithm.max_gen > smallConfig.algorithm.max_gen, ...
    'independent medium max_gen should be larger than small.');
assert(any(formalConfig.random.seedList == formalConfig.random.currentSeed), ...
    'independent formal currentSeed should be in seedList.');

afterFiles = dir(projectRoot);
afterNames = sort({afterFiles.name});
assert(isequal(beforeNames, afterNames), ...
    'test_independent_experiment_configs created project-root files.');

fprintf(['test_independent_experiment_configs passed: ', ...
    'small(pop=%d,max_gen=%d), medium(pop=%d,max_gen=%d), ', ...
    'formal(pop=%d,max_gen=%d,currentSeed=%g)\n'], ...
    smallConfig.algorithm.pop, smallConfig.algorithm.max_gen, ...
    mediumConfig.algorithm.pop, mediumConfig.algorithm.max_gen, ...
    formalConfig.algorithm.pop, formalConfig.algorithm.max_gen, ...
    formalConfig.random.currentSeed);

function assert_independent_config(config, projectRoot, expectedRunType, ...
    expectedOutputName)
assert(isstruct(config), 'Config should be a struct.');
assert(isfield(config, 'experiment'), 'Config missing experiment.');
assert(isfield(config, 'dataset'), 'Config missing dataset.');
assert(isfield(config, 'paths'), 'Config missing paths.');
assert(isfield(config, 'random'), 'Config missing random.');
assert(isfield(config, 'algorithm'), 'Config missing algorithm.');
assert(isfield(config, 'energy'), 'Config missing energy.');
assert(isfield(config, 'output'), 'Config missing output.');

assert(strcmp(config.experiment.runType, expectedRunType), ...
    'Unexpected independent runType.');
assert(strcmp(config.algorithm.name, 'Independent NSGA-II'), ...
    'Independent config should use Independent NSGA-II algorithm name.');

assert(isfile(config.paths.fjsp), 'Configured .fjs file does not exist.');
assert(isfile(config.paths.machineExcel), ...
    'Configured machine Excel file does not exist.');
assert(isfile(config.paths.agvExcel), ...
    'Configured AGV Excel file does not exist.');
assert(isfolder(config.paths.implementationDir), ...
    'Configured implementationDir does not exist.');
assert(~isfield(config.paths, 'algorithmDir'), ...
    'Independent config should not require raw algorithmDir.');

expectedOutputBaseDir = fullfile(projectRoot, 'outputs', expectedOutputName);
assert(strcmp(config.paths.outputBaseDir, expectedOutputBaseDir), ...
    'Independent outputBaseDir does not match expected outputs path.');

assert(isnumeric(config.random.seed) && isscalar(config.random.seed), ...
    'random.seed should be a numeric scalar.');
assert(isnumeric(config.random.currentSeed) && ...
    isscalar(config.random.currentSeed), ...
    'random.currentSeed should be a numeric scalar.');
assert(isnumeric(config.random.seedList) && ...
    all(isfinite(config.random.seedList(:))), ...
    'random.seedList should be finite.');

requiredAlgorithmFields = {'pop', 'max_gen', 'p_cross', 'p_mutation'};
for i = 1:numel(requiredAlgorithmFields)
    fieldName = requiredAlgorithmFields{i};
    assert(isfield(config.algorithm, fieldName), ...
        'Config algorithm missing field: %s.', fieldName);
end
assert(config.algorithm.pop > 0 && ...
    config.algorithm.pop == floor(config.algorithm.pop), ...
    'pop should be a positive integer.');
assert(config.algorithm.max_gen > 0 && ...
    config.algorithm.max_gen == floor(config.algorithm.max_gen), ...
    'max_gen should be a positive integer.');
assert(config.algorithm.p_cross >= 0 && config.algorithm.p_cross <= 1, ...
    'p_cross should be between 0 and 1.');
assert(config.algorithm.p_mutation >= 0 && ...
    config.algorithm.p_mutation <= 1, ...
    'p_mutation should be between 0 and 1.');

assert(config.energy.AGVEG_MAX > 0, 'AGVEG_MAX should be positive.');
assert(config.energy.eChargeSpeed > 0, 'eChargeSpeed should be positive.');
assert(config.output.saveSummary && config.output.saveMat && ...
    config.output.saveRunInfo, ...
    'Independent runs should save summary, mat result, and run_info.');
end
